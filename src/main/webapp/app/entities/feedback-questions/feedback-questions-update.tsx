import React, { useState, useEffect } from 'react';
import { Link, useNavigate, useParams } from 'react-router-dom';
import { Button, Row, Col, FormText } from 'reactstrap';
import { isNumber, ValidatedField, ValidatedForm } from 'react-jhipster';
import { FontAwesomeIcon } from '@fortawesome/react-fontawesome';

import { convertDateTimeFromServer, convertDateTimeToServer, displayDefaultDateTime } from 'app/shared/util/date-utils';
import { mapIdList } from 'app/shared/util/entity-utils';
import { useAppDispatch, useAppSelector } from 'app/config/store';

import { IEmployees } from 'app/shared/model/employees.model';
import { getEntities as getEmployees } from 'app/entities/employees/employees.reducer';
import { IFeedbackQuestions } from 'app/shared/model/feedback-questions.model';
import { getEntity, updateEntity, createEntity, reset } from './feedback-questions.reducer';

export const FeedbackQuestionsUpdate = () => {
  const dispatch = useAppDispatch();

  const navigate = useNavigate();

  const { id } = useParams<'id'>();
  const isNew = id === undefined;

  const employees = useAppSelector(state => state.employees.entities);
  const feedbackQuestionsEntity = useAppSelector(state => state.feedbackQuestions.entity);
  const loading = useAppSelector(state => state.feedbackQuestions.loading);
  const updating = useAppSelector(state => state.feedbackQuestions.updating);
  const updateSuccess = useAppSelector(state => state.feedbackQuestions.updateSuccess);

  const handleClose = () => {
    navigate('/feedback-questions' + location.search);
  };

  useEffect(() => {
    if (isNew) {
      dispatch(reset());
    } else {
      dispatch(getEntity(id));
    }

    dispatch(getEmployees({}));
  }, []);

  useEffect(() => {
    if (updateSuccess) {
      handleClose();
    }
  }, [updateSuccess]);

  const saveEntity = values => {
    values.createdat = convertDateTimeToServer(values.createdat);
    values.updatedat = convertDateTimeToServer(values.updatedat);

    const entity = {
      ...feedbackQuestionsEntity,
      ...values,
      employee: employees.find(it => it.id.toString() === values.employee.toString()),
    };

    if (isNew) {
      dispatch(createEntity(entity));
    } else {
      dispatch(updateEntity(entity));
    }
  };

  const defaultValues = () =>
    isNew
      ? {
          createdat: displayDefaultDateTime(),
          updatedat: displayDefaultDateTime(),
        }
      : {
          ...feedbackQuestionsEntity,
          createdat: convertDateTimeFromServer(feedbackQuestionsEntity.createdat),
          updatedat: convertDateTimeFromServer(feedbackQuestionsEntity.updatedat),
          employee: feedbackQuestionsEntity?.employee?.id,
        };

  return (
    <div>
      <Row className="justify-content-center">
        <Col md="8">
          <h2 id="vendianMonoApp.feedbackQuestions.home.createOrEditLabel" data-cy="FeedbackQuestionsCreateUpdateHeading">
            Create or edit a Feedback Questions
          </h2>
        </Col>
      </Row>
      <Row className="justify-content-center">
        <Col md="8">
          {loading ? (
            <p>Loading...</p>
          ) : (
            <ValidatedForm defaultValues={defaultValues()} onSubmit={saveEntity}>
              {!isNew ? (
                <ValidatedField name="id" required readOnly id="feedback-questions-id" label="ID" validate={{ required: true }} />
              ) : null}
              <ValidatedField
                label="Question"
                id="feedback-questions-question"
                name="question"
                data-cy="question"
                type="text"
                validate={{
                  maxLength: { value: 65535, message: 'This field cannot be longer than 65535 characters.' },
                }}
              />
              <ValidatedField
                label="Isdefault"
                id="feedback-questions-isdefault"
                name="isdefault"
                data-cy="isdefault"
                check
                type="checkbox"
              />
              <ValidatedField
                label="Area"
                id="feedback-questions-area"
                name="area"
                data-cy="area"
                type="text"
                validate={{
                  maxLength: { value: 255, message: 'This field cannot be longer than 255 characters.' },
                }}
              />
              <ValidatedField
                label="Competency"
                id="feedback-questions-competency"
                name="competency"
                data-cy="competency"
                type="text"
                validate={{
                  maxLength: { value: 255, message: 'This field cannot be longer than 255 characters.' },
                }}
              />
              <ValidatedField label="Category" id="feedback-questions-category" name="category" data-cy="category" type="text" />
              <ValidatedField label="Isskill" id="feedback-questions-isskill" name="isskill" data-cy="isskill" check type="checkbox" />
              <ValidatedField label="Skilltype" id="feedback-questions-skilltype" name="skilltype" data-cy="skilltype" type="text" />
              <ValidatedField
                label="Createdat"
                id="feedback-questions-createdat"
                name="createdat"
                data-cy="createdat"
                type="datetime-local"
                placeholder="YYYY-MM-DD HH:mm"
                validate={{
                  required: { value: true, message: 'This field is required.' },
                }}
              />
              <ValidatedField
                label="Updatedat"
                id="feedback-questions-updatedat"
                name="updatedat"
                data-cy="updatedat"
                type="datetime-local"
                placeholder="YYYY-MM-DD HH:mm"
                validate={{
                  required: { value: true, message: 'This field is required.' },
                }}
              />
              <ValidatedField id="feedback-questions-employee" name="employee" data-cy="employee" label="Employee" type="select">
                <option value="" key="0" />
                {employees
                  ? employees.map(otherEntity => (
                      <option value={otherEntity.id} key={otherEntity.id}>
                        {otherEntity.id}
                      </option>
                    ))
                  : null}
              </ValidatedField>
              <Button tag={Link} id="cancel-save" data-cy="entityCreateCancelButton" to="/feedback-questions" replace color="info">
                <FontAwesomeIcon icon="arrow-left" />
                &nbsp;
                <span className="d-none d-md-inline">Back</span>
              </Button>
              &nbsp;
              <Button color="primary" id="save-entity" data-cy="entityCreateSaveButton" type="submit" disabled={updating}>
                <FontAwesomeIcon icon="save" />
                &nbsp; Save
              </Button>
            </ValidatedForm>
          )}
        </Col>
      </Row>
    </div>
  );
};

export default FeedbackQuestionsUpdate;
